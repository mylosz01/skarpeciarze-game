package com.skarpeta.skarpeciarzegame;

public class ColorPalette {

    ColorShades green;
    ColorShades blue;
    ColorShades gray;
    ColorShades yellow;
    public ColorPalette(ColorShades green, ColorShades blue, ColorShades gray, ColorShades yellow) {
        this.green = green;
        this.blue = blue;
        this.gray = gray;
        this.yellow = yellow;
    }
}
